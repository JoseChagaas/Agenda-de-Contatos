#Criando dicionario de exemplo
# Dicionários são estruturas de dados que armazenam pares de chave-valor.
pessoa = {"nome": "José", "idade": 26, "cidade": "Rio de Janeiro"}
print("Meu dicionário de exemplo:", pessoa)

#Acessando valores por chave
print("Nome:", pessoa["nome"])
print("Idade:", pessoa["idade"])
print("Cidade:", pessoa["cidade"])
pessoa["sobrenome"] = "Lucas"
print("Sobrenome:", pessoa["sobrenome"])
      
          