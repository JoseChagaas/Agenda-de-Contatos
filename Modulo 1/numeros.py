#Inteiro
numero_inteiro = 3
print("Inteiro =", numero_inteiro)
#Real com ponto flutuante
numero_real = 3.14
print("Real =", numero_real)

# type() Exibe o tipo da variavel configurada 
print("Tipo da variavel inteiro = ", type(numero_inteiro))
print("Tipo da variavel real = ", type(numero_real))    

#Soma
soma = 1 + 1
print("1 + 1 =",soma)

#Subtração
subtracao = 1 - 1
print("1 - 1 =", subtracao)

#multiplicação
multiplicacao = 2 * 2
print("2 x 2 =", multiplicacao)

#divisão
divisao = 10 / 2
print("10 / 2 =", divisao)
print("Tipo da variavel do resultado da divisao é", type(divisao))
print("O valor da divisão em número inteiro é", int(divisao))
print(type(int(divisao)))

#Modulo (Resto) %

modulo = 5 % 2
print("O resto da divisão de 5 / 2 é", modulo)

#divisão Inteiro
divisao = 10 // 2
print("10 / 2 =", divisao)
print("Tipo da variavel do resultado da divisao é", type(divisao))
print("O valor da divisão em número inteiro é", int(divisao))
print(type(int(divisao)))
